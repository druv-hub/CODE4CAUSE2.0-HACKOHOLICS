def suggest_soils(state_name):
    # Check if the state exists in the dictionary
    if state_name in indiaSoilTypes:
        state_soils = indiaSoilTypes[state_name]

    # Create the suggested list by removing the state soils from soilTypes
    suggested = [soil for soil in soilTypes if soil in state_soils]
    total = [soil for soil in soilTypes if soil not in state_soils]

    return suggested,total

