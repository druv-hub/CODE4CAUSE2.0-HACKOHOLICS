def convert_to_hectares(area, unit):
    if unit == 'hectares':
        return area
    elif unit == 'acres':
        return int(area) * 0.404686
    elif unit == 'square-feet':
        return int(area) * 0.000092903
    elif unit == 'square-meters':
        return int(area) * 0.0001

