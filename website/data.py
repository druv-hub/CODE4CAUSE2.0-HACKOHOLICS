def convert_to_words_rounded(num):
    num=int(num)
    # Helper arrays for the place value names
    higher_units = ['', 'Thousand', 'Lacs', 'Cr']

    # Rounding logic to nearest lacs or thousands
    def round_number(num):
        if num >= 100000:
            return round(num, -5)  # Round to the nearest lakh
        elif num >= 1000:
            return round(num, -3)  # Round to the nearest thousand
        return num

    # Main conversion logic
    def number_to_words(num):
        if num == 0:
            return 'zero'

        # Apply rounding
        num = round_number(num)

        result = []
        place_values = []
        place_values.append(num % 1000)  # Get the first three digits (units, tens, hundreds)
        num //= 1000

        # Handle the higher places (thousand, lacs, crore)
        while num > 0:
            place_values.append(num % 100)  # Get the next two digits (thousand, lacs, etc.)
            num //= 100

        # Convert the numbers to words for each place value
        for i in range(len(place_values)):
            if place_values[i] != 0:
                if i == 0:
                    result.append(str(place_values[i]))
                else:
                    if i < len(higher_units):
                        result.append(str(place_values[i]) + ' ' + higher_units[i])
                    else:
                        # In case the number exceeds supported place values, just append the number
                        result.append(str(place_values[i]))

        return ' '.join(result[::-1]).strip()

    return number_to_words(num)

def costing(plant, area):
    plantationcost = convert_to_words_rounded(area*plant_seed_cost[plant])
    revenue = convert_to_words_rounded(area*plant_revenue[plant])
    return [plantationcost, revenue]

